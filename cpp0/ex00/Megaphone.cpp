#include <iostream>
#include <string>

int	main(int argc, char **argv)
{
	char c;
	if (argc == 1)
	{
		std::cout << "* LOUD AND UNBEARABLE FEEDBACK NOISE *" << std::endl;
		return (0);
	}
	for (int i = 1; i < argc; i++)
	{
		for (int j = 0; argv[i][j]; j++)
		{
			c = toupper(argv[i][j]);
			std::cout << c;
		}
	}
	std::cout << std::endl;
	return (0);
}
